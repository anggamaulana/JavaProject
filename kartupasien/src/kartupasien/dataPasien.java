    public void setData(){
        if(this.mode == dataPasien.UPDATE){
           
            this.identitasPasien = db.ambilDataPasien(this.idPasien);
            ed_nama_pasien.setText(this.identitasPasien[0]);
            txt_alamat.setText(this.identitasPasien[1]);
            ed_umur.setText(this.identitasPasien[2]);
            if(this.identitasPasien[3].compareTo("Laki-Laki")==0){
                cb_kelamin.setSelectedIndex(0);
            }else{
                cb_kelamin.setSelectedIndex(1);
            }
            ed_nama_pasien.setEditable(false);
            txt_alamat.setEditable(false);
            ed_umur.setEditable(false);
            cb_kelamin.setEnabled(false);
            
            cb_kelamin.setBackground(Color.white);
            cb_kelamin.setForeground(Color.white);
            ed_nama_pasien.setBackground(Color.white);
            ed_umur.setBackground(Color.white);
            
            Thread load = new Thread(new worker(worker.TABELRIWAYATPASIEN, this, this.db,this.idPasien));
            load.start();
            
            
                        
             
            
        }else if(this.mode == dataPasien.SHOW){
            btn_simpan_datapasien.setVisible(false);
        }else if(this.mode == dataPasien.INSERT){
            Object[] col = new Object[]{"ID","tanggal","diagnose","obat","tindakan","keterangan"};
            DefaultTableModel dm = new DefaultTableModel(col, 0);            
            tabel_riwayat.setModel(dm);
            btn_chooser.setEnabled(false);
            
        }
    }
    
    
    public void tambahbaris(String tanggal,String diagnose,String obat,String tindakan,String keterangan,char tipegigi,String gigi,int harga,Image img){
        if(this.idbarisbaru==0){
               this.idbarisbaru = db.notterakhir()+1;
        }else{
            this.idbarisbaru++;
        }
        DefaultTableModel dm = (DefaultTableModel)tabel_riwayat.getModel();
       
        rowTambahan.add(new Object[]{this.idbarisbaru,this.idPasien,tanggal,diagnose,obat,tindakan,keterangan,tipegigi,gigi,harga,img});
        rowIDNew.add(this.idbarisbaru);
        
            dm.addRow(new Object[]{this.idbarisbaru,tanggal,diagnose,obat,tindakan,keterangan,tipegigi,harga});
        
        tabel_riwayat.setModel(dm);
    
    }
    
    
    public void setIdPasien(int idpasien){
        for(int i=0;i<rowTambahan.size();i++){
            rowTambahan.get(i)[1] = idpasien;
        }
    }
    
   
    private Object cariObject(int id_baris){
        for(int i=0;i<rowTambahan.size();i++){
            if(rowTambahan.get(i)[0].equals(id_baris))
                return rowTambahan.get(i);
        }
        return null;
    }
