    private void btn_simpan_datapasienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpan_datapasienActionPerformed
        // TODO add your handling code here:
        if(this.mode == dataPasien.INSERT){
            
            try{
                String nama = ed_nama_pasien.getText();
                String alamat = txt_alamat.getText();
                int umur = Integer.parseInt(ed_umur.getText());
                String kelamin = (String)cb_kelamin.getSelectedItem();
                
                if(nama.compareTo("")==0 || alamat.compareTo("")==0 || kelamin.compareTo("")==0){
                   JOptionPane.showMessageDialog(this.parent,"Silahkan diisi identitas pasien dengan lengkap");
                }else{
                
                    db.tambahdatapasien(nama, alamat, umur, kelamin); 
                    setIdPasien(db.notterakhirpasien());
                    db.tambahRiwayatpasien(this.rowTambahan);
                    TableModel tm = db.ambilDataPasien("");
                    
                    this.parent.refreshdatatabel(tm);
                    dispose();
                }
            }catch(NumberFormatException ne){
                JOptionPane.showMessageDialog(this.parent, "Umur harus angka");
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this.parent,ex);
            }finally{
                
            }
        }else if(this.mode == dataPasien.UPDATE){
            try{      
                
                
                db.tambahRiwayatpasien(this.rowTambahan);
                TableModel tm = db.ambilDataPasien("");
                this.parent.refreshdatatabel(tm);
                dispose();
            }catch(NumberFormatException ne){
                JOptionPane.showMessageDialog(this.parent, "Umur harus angka");
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this.parent,ex);
            }finally{
                
            }
        }
        
        
        
    }//GEN-LAST:event_btn_simpan_datapasienActionPerformed

    private void btn_hapus_recordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapus_recordActionPerformed
        // TODO add your handling code here:
       try{
        int id_riwayat = (Integer)tabel_riwayat.getModel().getValueAt(tabel_riwayat.getSelectedRow(), 0);
        
            if(rowIDNew.contains(id_riwayat)){
                DefaultTableModel dtm = (DefaultTableModel)tabel_riwayat.getModel();
                dtm.removeRow(tabel_riwayat.getSelectedRow());
                rowTambahan.remove(cariObject(id_riwayat));
                if(this.idbarisbaru>0 && this.idbarisbaru==id_riwayat)
                    this.idbarisbaru--;
            }else{
                JOptionPane.showMessageDialog(this.parent, "Data riwayat Kesehatan tersebut sudah tercatat sebelumnya dan tidak bisa dihapus");
            }
            
       }catch(ArrayIndexOutOfBoundsException ar){
           JOptionPane.showMessageDialog(this.parent, "Silahkan Pilih data yang baru saja ditambahkan karena data riwayat pasien yang sudah tercatat tidak bisa di hapus");
           
       }
        
            
        
    }//GEN-LAST:event_btn_hapus_recordActionPerformed

